class Solution {
  public:
    int nthFibonacci(int n){
        // code here
        int m=1e9+7;
        int x=1;
        int y=1;
        int ans=1;
        for(int i=3;i<=n;i++){
            ans=((x+y)*1LL)%m;
            x=y;
            y=ans;
        }
        return ans;
    }
};

